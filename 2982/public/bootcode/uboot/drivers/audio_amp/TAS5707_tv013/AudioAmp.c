static ErrCode AudioAmp_AdoAmp_Tas5711L_write(UINT8 i2c_master,UINT8 w_len, UINT8* write_buf)
{
	if (!m_AMP_DEVICE_ADDR)
		return R_ERR_RX_ERROR;

	UINT8 addr[2],data[32],i;
	addr[0] = write_buf[0];

	for(i=1;i<w_len;i++)
		data[i-1] = write_buf[i];

	//printf("AudioAmp_AdoAmp_Tas5711L_write!!!!!\n");
	if(i2c_master < PCB_I2C_3)	{
		if (I2C_Write_EX(i2c_master, m_AMP_DEVICE_ADDR, w_len, write_buf) != S_OK) {
			return R_ERR_RX_ERROR;
		}
	}
	else		{
		//printf("G2C_Write_EX!!!!!\n");
		if (G2C_Write_EX(i2c_master, m_AMP_DEVICE_ADDR, 1, addr, w_len-1, data) != S_OK) {
			return R_ERR_RX_ERROR;
		}
	}

	return R_ERR_SUCCESS;
}

